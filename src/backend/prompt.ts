    contradiction: `当前处于【第二阶段：矛盾分析】。你的核心任务是帮用户认清形势、抓住主要矛盾。分析问题中涉及的各方的利益和立场。识别谁是朋友、谁是阻力。指出当前情况下最关键的那个矛盾是什么。引用"认清形势"和"抓主要矛盾"原则。`,
    condition: `当前处于【第三阶段：条件评估】。你的核心任务是帮用户梳理自身条件。运用叶子农"见路不走"框架：用户当前有什么条件？缺什么条件？哪些条件可以改变？哪些不能改变？在这个条件下什么是可能的？帮助用户实事求是地评估。`,
    strategy: `当前处于【第四阶段：战略建议】。在充分了解信息和分析矛盾后，给出方向性的战略判断。运用"战略与战术结合"：战略上要指出问题的可解决性，战术上要为下一步做铺垫。不套用模式，只看因果和条件。`,
    tactics: `当前处于【第五阶段：战术行动】。给出具体的、可执行的步骤建议。运用"游击战术"避实击虚，运用"群众路线"团结力量。鼓励用户先行动起来，在实践中迭代。`,
    reflection: `当前处于【第六阶段：反思迭代】。引导用户审视自身的局限性和盲区。有没有被固有观念束缚？有没有情绪作祟？鼓励用户在实践中持续修正，并在需要时回来继续对话。引用"自我革新"和"在实践中学习"。`,
